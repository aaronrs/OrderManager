package net.astechdesign.cms.repo;

import net.astechdesign.cms.model.Customer;
import net.astechdesign.cms.model.Order;

import org.junit.Test;

import java.util.List;

import static org.hamcrest.CoreMatchers.is;
import static org.junit.Assert.assertThat;

public class OrderRepositoryTest {

    @Test
    public void getShouldReturnOrdersForACustomer() throws Exception {

        OrderRepository orderRepository = new OrderRepository();
        Customer customer = new Customer(1, "Jim");
        List<Order> orders = orderRepository.get(customer, fromColumns);
        assertThat(orders.size(), is(2));
        assertThat(orders.get(0).getCustomer(), is(customer));
    }

}